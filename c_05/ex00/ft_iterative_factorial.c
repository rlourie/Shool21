int	ft_iterative_factorial(int nb)
{
	int	buf;

	buf = 1;
	if ((nb < 0))
	{
		return (0);
	}
	while (nb > 0)
	{
		buf = buf * nb;
		nb--;
	}
	return (buf);
}
