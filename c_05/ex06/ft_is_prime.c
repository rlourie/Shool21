int	ft_is_prime(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	if ((nb == 0) || (nb == 1))
		return (0);
	while (i != nb)
	{
		if (((nb % i) == 0) && (i != 1) && (i != nb))
			return (0);
		i++;
	}
	return (1);
}
