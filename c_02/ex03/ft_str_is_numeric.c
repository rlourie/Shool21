int	ft_str_is_numeric(char *str)
{
	int	i;
	int	ans;

	ans = 0;
	i = 0;
	while (str[i])
	{
		if ((str[i] >= '0') && (str[i] <= '9'))
		{
			ans = 1;
		}
		else
		{
			ans = 0;
		}
		if (ans == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
